"""
En Python una expression es culquier cosa que pueda calcularse para,
develover un valor para la linea
"""

a = 12
b = 3

print(a + b)  # 15
print(a - b)  # 9
print(a * b)  # 36
print(a / b)  # 4.0
print(a // b)  # 4 Integer divison, rounded down towards minus infinity "DIVISON ENTERA"
print(a % b)  # 0 modulo: the remainder after integer divison

